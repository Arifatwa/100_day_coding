hari = input("masukkan hari : ")

if hari == "senin":
    print("saya masih libur")
elif hari == "selasa":
    print("Dua mata kuliah")
elif hari == "rabu":
    print("Tiga mata kuliah")
elif hari == "kamis":
    print("Dua mata kuliah")
elif hari == "jum\'at":
    print("satu mata kuliah")
elif hari == "sabtu":
    print("Libur dari kuliah, tidak dengan Tugas")
elif hari == "minggu":
    print("Tidur memikirkan tugas")
