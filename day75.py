'''
program yang menghitung gaji bersih karyawan setiap bulannya
dengan ketentuan 
1. gaji pokok di masukkan melalui input
2. gaji lembur/jam adalah = 55000
3. lama lembur di masukka meggunakan fungsi input

'''

gaji_pokok = int(input("Masukkan gaji pokok :"))
gji_lbrperjam = 55000
lama_lembur = int(input("Masukkan lama lembur :"))

gaji_bersih = gaji_pokok + gji_lbrperjam * lama_lembur

print("gaji bersih : ", gaji_bersih)

